function _getdataset(arg){
	var temparr = [];
	if(arg.mark >= 6){
		for(let obj of dataarr) {
			if(obj.ORDERSTATUS >= 20){
				temparr.push(obj);
			}
		}
	}else{
		for(let obj of dataarr) {
			if(obj.ORDERSTATUS == arg.mark){
				temparr.push(obj);
			}
		}
	}
	return temparr;
}
exports.getDataArr = function(pageSize, pageNum, tabmark){
	var dataset = _getdataset({"mark": tabmark});
	if(dataset.length == 0){
		dataset = dataarr;
	}
	if(pageNum * pageSize < dataset.length){
		console.log((pageNum - 1) * pageSize);
		return {
			RESULTARR: dataset.slice((pageNum - 1) * pageSize,pageNum * pageSize),
			TOTAL: dataset.length,
		};
	}else if(pageNum * pageSize == dataset.length){
		console.log((Math.floor(dataset.length/pageSize)-1) * pageSize);
		return {
			RESULTARR: dataset.slice((Math.floor(dataset.length/pageSize)-1) * pageSize),
			TOTAL: dataset.length,
		};
	}else{
		console.log(Math.floor(dataset.length/pageSize) * pageSize);
		return {
			RESULTARR: dataset.slice(Math.floor(dataset.length/pageSize) * pageSize),
			TOTAL: dataset.length,
		};
	}
};
